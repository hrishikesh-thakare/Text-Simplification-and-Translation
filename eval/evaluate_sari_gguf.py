"""Evaluate GGUF simplifier with SARI on WikiLarge test set."""

from __future__ import annotations

import os
from difflib import SequenceMatcher
from pathlib import Path

import evaluate
import pandas as pd
from tqdm import tqdm

from simplify_gguf import GGUFTextSimplifier


README_START = "<!-- GGUF_SARI_START -->"
README_END = "<!-- GGUF_SARI_END -->"


def is_copy(source: str, prediction: str) -> bool:
    return SequenceMatcher(None, source, prediction).ratio() > 0.95


def resolve_test_csv() -> Path:
    here = Path(__file__).resolve().parent
    project_root = here.parent
    workspace_root = project_root.parent
    candidates = [
        project_root / "data" / "wikilarge_test.csv",
        workspace_root / "data" / "wikilarge_test.csv",
        workspace_root / "training" / "wikilarge_test.csv",
        project_root / "training" / "wikilarge_test.csv",
        here / "wikilarge_test.csv",
    ]
    for candidate in candidates:
        if candidate.exists():
            return candidate
    raise FileNotFoundError("wikilarge_test.csv not found in expected locations")


def resolve_readme() -> Path:
    return Path(__file__).resolve().parent.parent / "README.md"


def update_readme_with_gguf_sari(score: float, sample_count: int, copy_count: int) -> None:
    readme_path = resolve_readme()
    if not readme_path.exists():
        return

    block = (
        f"{README_START}\n"
        "## GGUF SARI Evaluation\n\n"
        f"- Dataset: WikiLarge test ({sample_count} samples)\n"
        f"- GGUF SARI: **{score:.2f}**\n"
        f"- Copy-like outputs (>0.95 similarity): {copy_count}\n\n"
        "Generated by `evaluate_sari_gguf.py`.\n"
        f"{README_END}"
    )

    content = readme_path.read_text(encoding="utf-8")
    if README_START in content and README_END in content:
        start = content.index(README_START)
        end = content.index(README_END) + len(README_END)
        content = content[:start] + block + content[end:]
    else:
        content = content.rstrip() + "\n\n" + block + "\n"

    readme_path.write_text(content, encoding="utf-8")


def main() -> None:
    print("Loading test dataset...")
    csv_path = resolve_test_csv()
    df = pd.read_csv(csv_path)

    max_samples_env = os.getenv("SARI_MAX_SAMPLES", "").strip()
    if max_samples_env:
        max_samples = int(max_samples_env)
        if max_samples > 0:
            df = df.head(max_samples)
            print(f"Using subset: {len(df)} samples")

    sources = df["Normal"].astype(str).tolist()
    references = [[str(ref)] for ref in df["Simple"].astype(str).tolist()]

    # Lowercase for stable SARI calculation.
    sources = [s.lower() for s in sources]
    references = [[r.lower() for r in ref] for ref in references]

    device = "cuda" if os.getenv("SARI_DEVICE", "cuda").lower() == "cuda" else "cpu"
    print(f"Loading GGUF simplifier on {device}...")
    simplifier = GGUFTextSimplifier(device=device)

    predictions: list[str] = []
    copy_count = 0

    print("Generating simplified outputs...")
    for i, text in enumerate(tqdm(sources)):
        simplified = simplifier.simplify_text(text)

        if not simplified or not isinstance(simplified, str):
            simplified = text

        simplified = simplified.lower().strip()
        if is_copy(text, simplified):
            copy_count += 1

        predictions.append(simplified)

        if i % 100 == 0:
            print("\nSample:")
            print("Input :", text)
            print("Output:", simplified)

    print(f"\nCopy-like outputs detected: {copy_count}")
    print("Calculating SARI...")
    sari_metric = evaluate.load("sari")
    results = sari_metric.compute(
        sources=sources,
        predictions=predictions,
        references=references,
    )

    print("--------------------------------------------------")
    print("GGUF SARI Evaluation Results")
    print(f"SARI Score: {results['sari']:.2f}")
    print("--------------------------------------------------")

    update_readme_with_gguf_sari(
        score=float(results["sari"]),
        sample_count=len(df),
        copy_count=copy_count,
    )
    print("README updated with GGUF SARI results")

    out_dir = Path(__file__).resolve().parent.parent / "outputs"
    out_dir.mkdir(parents=True, exist_ok=True)
    out_path = out_dir / "sari_eval_gguf_predictions.csv"
    out_df = df.copy()
    out_df["Predicted_GGUF"] = predictions
    out_df.to_csv(out_path, index=False)
    print(f"Saved predictions to {out_path}")


if __name__ == "__main__":
    main()
